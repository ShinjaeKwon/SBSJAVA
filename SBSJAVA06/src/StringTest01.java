public class StringTest01 {

	public static void main(String[] args) {
		String str = "안녕!";
		System.out.println(str);
		// "영희야 안녕!" 하고 철수가 말했습니다.
		String str2 = "\"영희야 안녕!\" 하고 철수가 말했습니다.";
		System.out.println(str2);
		
		// java 설치 경로는 C:\Program Files\Java\ 이다.
		String str3 = "java 설치 경로는 C:\\Program Files\\Java\\ 이다.";
		System.out.println(str3);
		// java 설치 경로는 "C:\\Program Files\\Java\\" 이다.
		String str4 = "java 설치 경로는 \"C:\\\\Program Files\\\\Java\\\\\" 이다.";
		System.out.println(str4);
	}

}