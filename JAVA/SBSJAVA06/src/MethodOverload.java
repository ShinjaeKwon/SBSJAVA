
public class MethodOverload {
	int plus(int num1, int num2){
		return num1 + num2;
	}
	
	
	int plus(int num1, int num2,int num3){
		return num1 + num2+num3;
	}
	
	
	double plus(double num1, double num2){
		return num1 + num2;
	}
	double plus(double num1, double num2, double num3){
		return num1 + num2 + num3;
	}
	
	
	
}
