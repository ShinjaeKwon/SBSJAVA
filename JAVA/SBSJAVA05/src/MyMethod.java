
public class MyMethod {
	
	//plus 메서드는 int 타입의 데이터를 두개를 입력받아서
	//두개 int 값을 더한 결과값을 내놓는 메서드이다.
	
	int plus(int a, int b){
		int result;
		result = a +b;
		return result;
	}
	
	int minus(int a, int b){
		int result;
		result = a -b;
		return result;
	}
	
	int devide(int a, int b){
		int result;
		result = a /b;
		return result;
	}
	int multiple(int a, int b){
		int result;
		result = a*b;
		return result;
	}
	
	int mod(int a,int b){
		int result;
		result = a%b;
		return result;
	}
	
}
